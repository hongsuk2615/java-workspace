package com.kh.test;

public class HelloWorld {
	
	/*
	 * 메인 메서드 : 자바 애플리케이션, 자바프로그램이 시작되는 시작점(entry point)
	 * 따라서 하나의 java애플리케이션에는 1개이상의 main 메서드가 포함된 클래스가 존재해야한다.
	 *
	 */
	
	public static void main(String[] args) {
		
		System.out.println("Hello World!!");
		
		//출력문 :  아래에 있는 console창에 ()에 들어간 "문자열" 값을 출력해줌
	}

}
